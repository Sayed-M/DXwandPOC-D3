partition(root);

const arc = d3
    .arc()
    .startAngle((d) => Math.max(0, Math.min(2 * Math.PI, d.x0)))
    .endAngle((d) => Math.max(0, Math.min(2 * Math.PI, d.x1)))
    .innerRadius((d) => (d.depth === 0 ? 0 : Math.max(0, d.y0 * 1)))
    .outerRadius((d) => (d.depth === 0 ? radius * 0.1 : Math.max(0, d.y1 * 1)));

